"""
This code demonstrates basic comparison operations in Python:
- Greater than: Checks if one number is greater than another.
- Equality: Compares the values of two strings and two numbers.
- Not equal: Checks if two numbers are different.
"""

# Compare functions
print(10 > 1)        # True: 10 is greater than 1
print("cat" == "dog")  # False: "cat" is not equal to "dog"
print(1 != 2)       # True: 1 is not equal to 2

#print(1 > "1")     # shows error (TypeError)
print(1 == "1")     # False: integer 1 is not equal to string "1"

